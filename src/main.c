#include "minishell.h"

int	main(int argc, char **argv, char **envp)
{
	t_shell	*shell;

	(void)argc;
	(void)argv;
	shell = init_shell(envp);
	if (!shell)
		return (1);
	setup_signal_handlers();
	shell_loop(shell);
	free_shell(shell);
	return (shell->exit_status);
}
